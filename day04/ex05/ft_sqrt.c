int		ft_sqrt(int nb)
{
	int max;
	int i;

	if (nb == 0 || nb == 1)
	{
		return (nb);
	}
	max = nb / 2;
	i = 2;
	while (i <= max)
	{
		if (nb == i * i)
			return (i);
		if (nb < i * i)
			break ;
		i += 1;
	}
	return (0);
}
