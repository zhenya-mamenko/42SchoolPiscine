int		ft_add(int value1, int value2)
{
	return (value1 + value2);
}

int		ft_sub(int value1, int value2)
{
	return (value1 - value2);
}

int		ft_mul(int value1, int value2)
{
	return (value1 * value2);
}

int		ft_div(int value1, int value2)
{
	return (value1 / value2);
}

int		ft_mod(int value1, int value2)
{
	return (value1 % value2);
}
