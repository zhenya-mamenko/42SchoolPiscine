long	add(long value1, long value2)
{
	return (value1 + value2);
}

long	substract(long value1, long value2)
{
	return (value1 - value2);
}

long	multiply(long value1, long value2)
{
	return (value1 * value2);
}

long	divide(long value1, long value2)
{
	return (value1 / value2);
}

long	modulo(long value1, long value2)
{
	return (value1 % value2);
}
