long	ft_add(long value1, long value2)
{
	return (value1 + value2);
}

long	ft_sub(long value1, long value2)
{
	return (value1 - value2);
}

long	ft_mul(long value1, long value2)
{
	return (value1 * value2);
}

long	ft_div(long value1, long value2)
{
	return (value1 / value2);
}

long	ft_mod(long value1, long value2)
{
	return (value1 % value2);
}
