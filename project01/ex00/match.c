int	match(char *s1, char *s2)
{
	int r;

	while (*s2 != '*' && *s2 && *s1)
	{
		if (*s1++ != *s2++)
			return (0);
	}
	if (!*s2)
		return (!*s1 ? 1 : 0);
	while (*s2 == '*')
		s2++;
	if (!*s2)
		return (1);
	while (*s1)
	{
		if (*s1 == *s2)
		{
			r = match(s1, s2);
			if (r)
				return (1);
		}
		s1++;
	}
	return (0);
}
